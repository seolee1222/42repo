#include"libft.h"

int	ft_isalnum(int c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		return (1);
	if ('0' <= c && c <= '9')
		return (1);
	return (0);
}
